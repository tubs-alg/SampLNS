def test_transaction_graph():
    tg = TransactionGraph(5)
    assert not tg.has_edge(-1, 3)
    assert tg.count_edges() == 0
    tg.add_edge(1, 2)
    assert tg.has_edge(1, 2)
    assert tg.count_edges() == 1
    tg.add_edge(3, 2)
    tg.add_edge(3, 1)
    assert tg.has_edge(2, 3)
    assert tg.has_edge(1, 3)
    assert tg.count_edges() == 3


